package com.cpm.delegates;

public class PerformanceDataBean {
	
	public String monthly_target, daily_target, achieved, pending;

	public String getMonthly_target() {
		return monthly_target;
	}

	public void setMonthly_target(String monthly_target) {
		this.monthly_target = monthly_target;
	}

	public String getDaily_target() {
		return daily_target;
	}

	public void setDaily_target(String daily_target) {
		this.daily_target = daily_target;
	}

	public String getAchieved() {
		return achieved;
	}

	public void setAchieved(String achieved) {
		this.achieved = achieved;
	}

	public String getPending() {
		return pending;
	}

	public void setPending(String pending) {
		this.pending = pending;
	}

}
