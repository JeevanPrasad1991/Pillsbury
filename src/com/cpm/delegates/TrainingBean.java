package com.cpm.delegates;

public class TrainingBean {
	public String training_id;
	public String training;
	public String salesperson;
	public String contact_cd;
	
	

	public String getContact_cd() {
		return contact_cd;
	}

	public void setContact_cd(String contact_cd) {
		this.contact_cd = contact_cd;
	}

	public String getSalesperson() {
		return salesperson;
	}

	public void setSalesperson(String salesperson) {
		this.salesperson = salesperson;
	}

	public String getTraining_id() {
		return training_id;
	}

	public void setTraining_id(String des_cd) {
		this.training_id = des_cd;
	}

	public String getTraining() {
		return training;
	}

	public void setTraining(String des) {
		this.training = des;
	}

}
